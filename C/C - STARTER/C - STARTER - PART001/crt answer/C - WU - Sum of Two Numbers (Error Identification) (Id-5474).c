#include <stdio.h>

int main() {
    int num1, num2;

    // Prompt the user to enter two integers
    printf(" ");
    
    // Read the two integers from input
    scanf("%d %d", &num1, &num2);

    // Calculate the sum of the two integers
    int sum = num1 + num2;

    // Print the sum
    printf(" %d\n", sum);

    return 0;
}
